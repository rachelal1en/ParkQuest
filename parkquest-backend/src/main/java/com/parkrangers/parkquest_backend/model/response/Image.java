package com.parkrangers.parkquest_backend.model.response;


public class Image {
    private String url;
    private String title;

    public Image() {}

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
